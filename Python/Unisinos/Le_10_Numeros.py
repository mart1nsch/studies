primeiro_numero = int(input("Primeiro número: "))

if primeiro_numero == 0:
    print("O número é zero!")
elif ((primeiro_numero / 2) - (int(primeiro_numero / 2))) > 0:
    print("O número é ímpar!")
elif ((primeiro_numero / 2) - (int(primeiro_numero / 2))) == 0:
    print("O número é par!")

